package Lesson5;

public class IntBox {
    private Integer intBox1;
    private Integer intBox2;
    private Integer intBox3;

    public IntBox(Integer intBox1, Integer intBox2, Integer intBox3) {
        this.intBox1 = intBox1;
        this.intBox2 = intBox2;
        this.intBox3 = intBox3;
    }

    public Integer getIntBox1() {
        return intBox1;
    }

    public Integer getIntBox2() {
        return intBox2;
    }

    public Integer getIntBox3() {
        return intBox3;
    }
}
